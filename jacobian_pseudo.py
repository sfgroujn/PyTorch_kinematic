"""
jacobian_pseudo.py
Матрица Якоби и псевдообратная матрица для управления роботом.
Демонстрация: как из желаемой скорости схвата получить скорости сочленений.
"""

import torch
import pytorch_kinematics as pk
import numpy as np
import matplotlib.pyplot as plt

# ============================================================
# 1. ЗАГРУЗКА МОДЕЛИ РОБОТА
# ============================================================
print("=" * 60)
print("1. ЗАГРУЗКА МОДЕЛИ РОБОТА")
print("=" * 60)

urdf_path = "robots/my_robot.urdf"
chain = pk.build_serial_chain_from_urdf(open(urdf_path).read(), "tool0")

print(f"Сочленения: {chain.get_joint_parameter_names()}")
print(f"Количество DOF: {chain.n_joints}")

# Фиксированная конфигурация робота (30°, 45°)
theta1 = 30 * np.pi / 180
theta2 = 45 * np.pi / 180
q = torch.tensor([theta1, theta2])

print(f"Конфигурация: θ1 = {theta1:.3f} рад ({theta1*180/np.pi:.0f}°), "
      f"θ2 = {theta2:.3f} рад ({theta2*180/np.pi:.0f}°)")

# ============================================================
# 2. ПРЯМАЯ МАТРИЦА ЯКОБИ (J)
# ============================================================
print("\n" + "=" * 60)
print("2. ПРЯМАЯ МАТРИЦА ЯКОБИ (J) — ЧТО ПОКАЗЫВАЕТ")
print("=" * 60)

J = chain.jacobian(q).squeeze(0)  # (6, 2)

print("Размер J: 6 строк (скорость схвата) × 2 столбца (сочленения)")
print("\nJ (линейная скорость, м/с):")
print(f"  vx = {J[0,0]:7.3f}·θ̇₁ + {J[0,1]:7.3f}·θ̇₂")
print(f"  vy = {J[1,0]:7.3f}·θ̇₁ + {J[1,1]:7.3f}·θ̇₂")

print("\nИнтерпретация:")
print(f"  • Если θ̇₁ = 1 рад/с, то схват едет: vx = {J[0,0]:.2f} м/с, vy = {J[1,0]:.2f} м/с")
print(f"  • Если θ̇₂ = 1 рад/с, то схват едет: vx = {J[0,1]:.2f} м/с, vy = {J[1,1]:.2f} м/с")

# ============================================================
# 3. ПСЕВДООБРАТНАЯ МАТРИЦА (J⁺)
# ============================================================
print("\n" + "=" * 60)
print("3. ПСЕВДООБРАТНАЯ МАТРИЦА (J⁺) — КАК УПРАВЛЯТЬ")
print("=" * 60)

# Берём только позиционную часть якобиана (первые 3 строки)
J_pos = J[:3, :]  # (3, 2)
J_plus = torch.linalg.pinv(J_pos)  # (2, 3)

print("Размер J⁺: 2 строки (сочленения) × 3 столбца (vx, vy, vz)")
print("\nJ⁺ (как каждое сочленение реагирует на движение схвата):")
print(f"  θ̇₁ = {J_plus[0,0]:7.3f}·vx + {J_plus[0,1]:7.3f}·vy + {J_plus[0,2]:7.3f}·vz")
print(f"  θ̇₂ = {J_plus[1,0]:7.3f}·vx + {J_plus[1,1]:7.3f}·vy + {J_plus[1,2]:7.3f}·vz")

# ============================================================
# 4. УПРАВЛЕНИЕ СКОРОСТЬЮ
# ============================================================
print("\n" + "=" * 60)
print("4. УПРАВЛЕНИЕ СКОРОСТЬЮ — ПРИМЕРЫ")
print("=" * 60)

# Пример 1: Движение вперёд по X
v1 = torch.tensor([0.1, 0.0, 0.0])
q_dot1 = J_plus @ v1
print(f"\n1. Желаем: vx = 0.1 м/с (вперёд)")
print(f"   Нужные скорости сочленений: θ̇₁ = {q_dot1[0]:.4f} рад/с, θ̇₂ = {q_dot1[1]:.4f} рад/с")
print(f"   Проверка (J·q̇): vx = {(J_pos @ q_dot1)[0]:.4f} м/с (должно быть 0.1)")

# Пример 2: Движение вверх по Y
v2 = torch.tensor([0.0, 0.1, 0.0])
q_dot2 = J_plus @ v2
print(f"\n2. Желаем: vy = 0.1 м/с (вверх)")
print(f"   Нужные скорости сочленений: θ̇₁ = {q_dot2[0]:.4f} рад/с, θ̇₂ = {q_dot2[1]:.4f} рад/с")
print(f"   Проверка: vy = {(J_pos @ q_dot2)[1]:.4f} м/с")

# Пример 3: Одновременное движение по X и Y
v3 = torch.tensor([0.05, 0.05, 0.0])
q_dot3 = J_plus @ v3
print(f"\n3. Желаем: vx = 0.05 м/с, vy = 0.05 м/с (диагональ)")
print(f"   Нужные скорости сочленений: θ̇₁ = {q_dot3[0]:.4f} рад/с, θ̇₂ = {q_dot3[1]:.4f} рад/с")

# ============================================================
# 5. СРАВНЕНИЕ РУЧНОЙ И ВСТРОЕННОЙ ПСЕВДООБРАТНОЙ
# ============================================================
print("\n" + "=" * 60)
print("5. СРАВНЕНИЕ МЕТОДОВ ВЫЧИСЛЕНИЯ ПСЕВДООБРАТНОЙ")
print("=" * 60)

# Ручная формула: J⁺ = (Jᵀ·J)⁻¹·Jᵀ (для n < 6)
J_T_J = J_pos.T @ J_pos
J_plus_manual = torch.linalg.inv(J_T_J) @ J_pos.T

# Встроенная функция
J_plus_builtin = torch.linalg.pinv(J_pos)

# Сравнение
print("Метод 1 (ручной):\n", J_plus_manual)
print("\nМетод 2 (torch.linalg.pinv):\n", J_plus_builtin)
print(f"\nРазница: {torch.max(torch.abs(J_plus_manual - J_plus_builtin)).item():.10f}")

# ============================================================
# 6. ВИЗУАЛИЗАЦИЯ МАТРИЦ ЯКОБИ
# ============================================================
print("\n" + "=" * 60)
print("6. ВИЗУАЛИЗАЦИЯ (графики сохранены)")
print("=" * 60)

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(12, 5))

# Тепловая карта прямой матрицы J
im1 = ax1.imshow(J_pos[:2, :].numpy(), cmap='RdBu', aspect='auto')
ax1.set_xticks([0, 1])
ax1.set_xticklabels(['θ̇₁ (joint 1)', 'θ̇₂ (joint 2)'])
ax1.set_yticks([0, 1])
ax1.set_yticklabels(['vx', 'vy'])
ax1.set_title('Прямая матрица Якоби J (влияние сочленений)')
plt.colorbar(im1, ax=ax1)

# Тепловая карта псевдообратной матрицы J⁺
im2 = ax2.imshow(J_plus[:2, :2].numpy(), cmap='RdBu', aspect='auto')
ax2.set_xticks([0, 1])
ax2.set_xticklabels(['vx', 'vy'])
ax2.set_yticks([0, 1])
ax2.set_yticklabels(['θ̇₁ (joint 1)', 'θ̇₂ (joint 2)'])
ax2.set_title('Псевдообратная матрица J⁺ (управление)')
plt.colorbar(im2, ax=ax2)

plt.tight_layout()
plt.savefig('jacobian_matrices.png')
print("График сохранён в 'jacobian_matrices.png'")

print("\n" + "=" * 60)
print("ГОТОВО! Запускайте файлы и экспериментируйте.")
print("=" * 60)